public class UnaryArithmatc {
    public static void main(String[] args) {
        //Pre-Increment
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);


        //Post Increment
        a = 10;
        b = a ++;
        System.out.println(a);
        System.out.println(b);
    }
}
