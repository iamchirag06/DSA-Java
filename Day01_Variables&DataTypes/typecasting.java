import java.util.*;
public class typecasting {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        float a = 25.999f;
        int b = (int)a;
        System.out.println(b);

        char ch = 'g';
        int c = ch;
        System.out.println(c);
        sc.close();
    }
}
